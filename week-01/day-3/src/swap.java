public class swap {
    public static void main(String[] args) {
        // Swap the values of the variables
        int a = 123;
        int b = 526;

        System.out.println("b = " + a);
        System.out.println("a = " + b);

        // Swap the values of the variables
        int c = 123;
        int d = 526;
        int e = c;
        c = d;
        d = e;

        System.out.println(c);
        System.out.println(d);
    }
}
